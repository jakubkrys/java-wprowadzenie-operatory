class Circuit {
	public static void main(String args[]){
		
		byte r = 7;
		double pi = 3.14;
		double circleCircuit;
		
		circleCircuit = 2*pi*r;
		System.out.println();
		System.out.println("-----Circle Circuit-----");
		System.out.println();
		System.out.println("Radius: "+r);
		System.out.println("Circle Circuit: "+circleCircuit);
		System.out.println();
		
	}
}